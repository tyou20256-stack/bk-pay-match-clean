// Backward compat
const I18N = translations;

let currentLang = localStorage.getItem('bkpay_lang') || localStorage.getItem('lang') || 'ja';

function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) || translations['ja'][key] || key;
}

function tf(key, ...args) {
  const fn = I18N_FN[currentLang]?.[key] || I18N_FN['ja']?.[key];
  if (typeof fn === 'function') return fn(...args);
  return t(key);
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('bkpay_lang', lang);
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;
  applyTranslations();
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  if (typeof render === 'function') render();
  if (typeof loadArbitrage === 'function') loadArbitrage();
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const v = t(key);
    if (v) el.textContent = v;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  const fa = document.getElementById('filterAmount');
  if (fa) fa.placeholder = t('filter_placeholder');
}

// Backward compat aliases
function applyI18n() { applyTranslations(); }
function toggleLang() { /* deprecated - use lang selector */ }

function detectLanguage() {
  const saved = localStorage.getItem('bkpay_lang') || localStorage.getItem('lang');
  if (saved && ['ja', 'en', 'zh', 'vi'].includes(saved)) return saved;
  const browserLang = navigator.language.split('-')[0];
  if (['ja', 'en', 'zh', 'vi'].includes(browserLang)) return browserLang;
  return 'ja';
}

// Initialize: apply translations immediately if DOM is ready, or wait
function initI18n() {
  currentLang = detectLanguage();
  applyTranslations();
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
}
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}
