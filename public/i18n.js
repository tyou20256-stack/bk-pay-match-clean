  ja: { buy_title: (c) => `購入（円 → ${c}）`, sell_title: (c) => `売却（${c} → 円）` },
  en: { buy_title: (c) => `Buy (JPY → ${c})`, sell_title: (c) => `Sell (${c} → JPY)` },
  zh: { buy_title: (c) => `购买（日元 → ${c}）`, sell_title: (c) => `出售（${c} → 日元）` },
